		<h2>
		Dargoens & Donguns (comumente abreviado como D&D ou DnD) é um jogo de interpretação (role-playing game, ou RPG) bem estruturado e cooperativo (normalmente não há vencedores), no qual, cada jogador controla, tipicamente, um único personagem. Durante o curso do jogo, cada jogador dirige as ações de seu personagem e suas interações com outros personagens. Uma partida geralmente dura varias sessões, que são chamadas de aventuras. Um conjunto de aventuras relacionadas entre si é chamada de campanha.
<p>Os resultados das escolhas do grupo e a história básica do jogo é determinada pelo Donguns Master (DM), também chamado de mestre do jogo, ou masmorra, ou ainda como narrador, de acordo com as regras e com a interpretação do DM sobre essas regras. O DM escolhe e descreve os vários Personagens Não Jogadores (PNJs), que os personagens encontram, o cenário em que essas relações ocorrem, e os resultados desses encontros baseados nas escolhas e ações dos jogadores. As extensas regras do jogo, que cobrem áreas diversas como interações sociais, uso de magia, combate e o efeito do ambiente nos personagens, ajudam o mestre em suas decisões. O DM pode escolher quais regras publicadas ele vai usar e até mesmo criar regras novas (conhecidas como homebrews), se achar necessário.</p>
